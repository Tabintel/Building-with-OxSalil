// A Function to Convert Seconds into Minutes

function convertSecondsToMinutes(seconds) {
    return Math.floor(seconds / 60);
  }
  
console.log(convertSecondsToMinutes(120)); // Output: 2
console.log(convertSecondsToMinutes(180)); // Output: 3
console.log(convertSecondsToMinutes(300)); // Output: 5  